    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $fatherNames = $this->getFatherNames();

        foreach ($fatherNames as $father) {
            $race = $this->detectRaceByFatherName($father->name);

            // Generate 25 child records for each father
            Childs::factory()
                ->count(25)
                ->state([
                    'parent_id' => $father->id,  // Only valid columns passed here
                ])
                ->create([
                    'child_name' => "{$father->name} Jr.",
                    'school_id' => rand(1, 10),
                    'dob' => now()->subYears(rand(3, 10)),
                    'child_gender' => 'Male', // Example value, adjust as needed
                ]);
        }
    }

    protected function getFatherNames()
    {
        // Select name where role is father
        return User::where('role', 'father')->select(['id', 'name'])->get();
    }

    protected function detectRaceByFatherName($fatherName)
    {
        // Logic to detect race based on father's name
        if (preg_match('/\b(bin|binti)\b/', $fatherName) || $this->isMalayName($fatherName)) {
            return 'Malay';
        }

        if ($this->isChineseName($fatherName)) {
            return 'Chinese';
        }

        if (preg_match('/\ba\/[lp]\b/', $fatherName) || $this->isIndianName($fatherName)) {
            return 'Indian';
        }

        return 'Others';
    }

    protected function isMalayName($name)
    {
        $malayNames = self::$firstNameMaleMalay;
        foreach ($malayNames as $malayName) {
            if (stripos($name, $malayName) !== false) {
                return true;
            }
        }
        return false;
    }

    protected function isChineseName($name)
    {
        $chineseSurnames = self::$firstNameChinese;
        foreach ($chineseSurnames as $surname) {
            if (stripos($name, $surname) !== false) {
                return true;
            }
        }
        return false;
    }

    protected function isIndianName($name)
    {
        $indianNames = self::$firstNameMaleIndian;
        foreach ($indianNames as $indianName) {
            if (stripos($name, $indianName) !== false) {
                return true;
            }
        }
        return false;
    }
